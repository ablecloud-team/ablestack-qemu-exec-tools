ftctl_config_apply_cli() {
  local cfg="${1-}"
  local pol="${2-}"
  local dry="${3-}"
  [[ -n "${cfg}" ]] && FTCTL_CONFIG_PATH="${cfg}"
  [[ -n "${pol}" ]] && FTCTL_POLICY="${pol}"
  [[ -n "${dry}" ]] && FTCTL_DRY_RUN="${dry}"
  return 0
}

ftctl_config_load_file() {
  local path="${1-}"
  [[ -n "${path}" && -f "${path}" ]] || return 0
  set -a
  # shellcheck source=/dev/null
  source "${path}"
  set +a
}

ftctl_config_finalize_paths() {
  if [[ -z "${FTCTL_LOCK_FILE}" || "${FTCTL_LOCK_FILE}" == "${FTCTL_LOCK_FILE_DEFAULT}" ]]; then
    FTCTL_LOCK_FILE="${FTCTL_RUN_DIR}/lock"
  fi
  if [[ -z "${FTCTL_STATE_DIR}" || "${FTCTL_STATE_DIR}" == "${FTCTL_STATE_DIR_DEFAULT}" ]]; then
    FTCTL_STATE_DIR="${FTCTL_RUN_DIR}/state"
  fi
  if [[ -z "${FTCTL_EVENTS_LOG}" || "${FTCTL_EVENTS_LOG}" == "${FTCTL_EVENTS_LOG_DEFAULT}" ]]; then
    FTCTL_EVENTS_LOG="${FTCTL_LOG_DIR}/events.log"
  fi
  if [[ -z "${FTCTL_CLUSTER_HOSTS_DIR}" || "${FTCTL_CLUSTER_HOSTS_DIR}" == "${FTCTL_CLUSTER_HOSTS_DIR_DEFAULT}" ]]; then
    FTCTL_CLUSTER_HOSTS_DIR="${FTCTL_CLUSTER_DIR}/hosts"
  fi
}

ftctl_ensure_runtime_dirs() {
  ftctl_ensure_dir "${FTCTL_RUN_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_LOG_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_STATE_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_PROFILE_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_CLUSTER_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_CLUSTER_HOSTS_DIR}" "0755"
  ftctl_ensure_dir "$(dirname "${FTCTL_EVENTS_LOG}")" "0755"
  ftctl_ensure_dir "${FTCTL_BLOCKCOPY_TARGET_BASE_DIR}" "0755"
  ftctl_ensure_dir "${FTCTL_XML_BACKUP_DIR}" "0755"
}
